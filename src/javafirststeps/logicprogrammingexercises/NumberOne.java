package javafirststeps.logicprogrammingexercises;

/**
 *
 * Un ciclo while que imprime los numeros del 1 al 10.
 */
public class NumberOne {

    public static void main(String[] args) {

        int numeros = 1;

        while (numeros <= 10) {

            System.out.println("el numero es: " + numeros);
            numeros++;

        }
    }

}
