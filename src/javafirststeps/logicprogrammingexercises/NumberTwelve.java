package javafirststeps.logicprogrammingexercises;

/**
 *
 * Imprima los multiplos de 3 usando un cilo for.
 */
public class NumberTwelve {
    public static void main(String[] args) {
        for (int i = 3; i <= 1000; i += 3) {
            System.out.println(i);
        }
    }
}
